static BaseType_t prvTaskStatsCommand( char *pcWriteBuffer,
                                          size_t xWriteBufferLen,
										  const char *pcCommandString )
{
    static BaseType_t state = 0;

    if (!state){
        /* For simplicity, this function assumes the output buffer is large enough
        to hold all the text generated by executing the vTaskList() API function,
        so the xWriteBufferLen parameter is not used. */
        char *head = "Name		State  Priority  Stack  Number\n\r";
        ( void ) xWriteBufferLen;

        /* pcWriteBuffer is used directly as the vTaskList() parameter, so the table
        generated by executing vTaskList() is written directly into the output
        buffer. */
        strcpy(pcWriteBuffer, head);
        vTaskList( &pcWriteBuffer[strlen(head)]);

        /* The entire table was written directly to the output buffer.  Execution
        of this command is complete, so return pdFALSE. */
        state = 1;
        return pdTRUE;
    }else{
        state = 0;
        strcpy(pcWriteBuffer, "\n\r");
        return pdFALSE;
    }
}

static BaseType_t prvIACommand( char *pcWriteBuffer,
                                  size_t xWriteBufferLen,
                                  const char *pcCommandString )
{
	const char *pcParameter1 = NULL;
	BaseType_t xParameter1StringLength;

    /* Obtain the name of the source file, and the length of its name, from
       the command string. The name of the source file is the first parameter. */
	pcParameter1 = FreeRTOS_CLIGetParameter( pcCommandString,
                                         1,
                                         &xParameter1StringLength );
	if (!strcmp(pcParameter1, "on")){
		start_mic_task = true;
		vTaskResume(MicAcquisitionHandle);
		sprintf(pcWriteBuffer, "IA Task started!\r\n");
	}else{
		if (!strcmp(pcParameter1, "off")){
			sprintf(pcWriteBuffer, "IA Task stopped!\r\n");
			start_mic_task = false;
		}else{
			if (pcParameter1 != NULL){
				uint32_t  p1, s2, cnt, size;
				bool first_block = true;
				char buffer[512];
				int16_t *pbuffer;

				if (f_open(&SDFile, pcParameter1, FA_READ) == FR_OK){
					p1 = f_size(&SDFile);

					uint32_t j = 0;
					while (p1)
					{
						if (p1 >= sizeof(buffer))
						{
							cnt = sizeof(buffer);
							p1 -= sizeof(buffer);
						}
						else
						{
							cnt = (uint16_t)p1;
							p1 = 0;
						}
						if (f_read(&SDFile, buffer, cnt, (unsigned int *)&s2) != FR_OK)
						{
							break;
						}else
						{
							if (first_block){
								pbuffer = (int16_t *)&buffer[sizeof(wav_header_t)];
								size = (s2 - sizeof(wav_header_t));
								first_block = false;
							}else{
								pbuffer = (int16_t *)buffer;
								size = s2;
							}
							for (int i=0; i<size; i += 2){
								//features[j++] = (float)(pbuffer[i] + (pbuffer[i+1] << 8));
								features[j++] = (float)(*pbuffer++);
							}
							if (cnt != s2) break;
						}
					}

					f_close(&SDFile);
					xSemaphoreGive(sem_new_data);
					sprintf(pcWriteBuffer, "Initializating %s audio classification!\r\n", pcParameter1);
				}else{
					sprintf(pcWriteBuffer, "%s does not exist!\r\n", pcParameter1);
				}
			}else{
				sprintf(pcWriteBuffer, "You need to specify a filename!\r\n");
			}
		}
	}

    /* There is only a single line of output produced in all cases. pdFALSE is
       returned because there is no more output to be generated. */
    return pdFALSE;
}

static BaseType_t prvAudioRecordCommand( char *pcWriteBuffer,
                                  size_t xWriteBufferLen,
                                  const char *pcCommandString )
{
	const char *filename;
	static int state = 0;
	BaseType_t xParameter1StringLength;

    /* Obtain the name of the source file, and the length of its name, from
       the command string. The name of the source file is the first parameter. */
	filename = FreeRTOS_CLIGetParameter( pcCommandString,
                                         1,
                                         &xParameter1StringLength );
	switch (state){
		case 0:
			if (filename != NULL){
				if (start_mic_task == false){
					(void)sprintf(pcWriteBuffer, "Recording...\r\n");
					state = 1;
					return pdTRUE;
				}else{
					(void)sprintf(pcWriteBuffer, "Microphone is already being used!\r\n");
				}
			}else {
				(void)sprintf(pcWriteBuffer, "You need to specify a filename!\r\n");
			}
			break;
		case 1:
			record_wav((char *)filename, 1);
			(void)sprintf(pcWriteBuffer, "%s recorded!\r\n", filename);
			state = 0;
			break;
	}

	return pdFALSE;
}

