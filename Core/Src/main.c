/**
  * @brief  Function implementing the MicAcquisition thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_mic_task */
void mic_task(void const * argument)
{
  /* USER CODE BEGIN 5 */
	  uint32_t bytes_read = 0;
	  uint32_t flag = 0;
	  mic_init();
	  /* Infinite loop */
	  for(;;)
	  {
		// Start microphone capture
	    mic_start();
	    while(1){
	    	if (flag == 0){
	    		flag = 1;
	    		print_service_send("Speak!\n", 0, 10);
	    	}
	    	bytes_read = mic_read_in_float();
	    	if (!raw_feature_set_data(get_read_float_buffer(), bytes_read)){
	    		mic_pause();
	    		//print_service_send("Acquisition concluded!\n", 0, 10);
	        	xSemaphoreGive(sem_new_data);
	        	vTaskDelay(1000);
	    		flag = 0;
	    		mic_resume();
	    	}
	    }
	  }
  /* USER CODE END 5 */
}

